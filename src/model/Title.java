package model;

public class Title {

	private int idTitle;
	private int parentIdTitle;
	private String title;
	
	public int getIdTitle() {
		return idTitle;
	}
	public void setIdTitle(int idTitle) {
		this.idTitle = idTitle;
	}
	public int getParentIdTitle() {
		return parentIdTitle;
	}
	public void setParentIdTitle(int parentIdTitle) {
		this.parentIdTitle = parentIdTitle;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
		
}
